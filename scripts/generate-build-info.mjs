// Writes the current build timestamp into the demo app so the deployed
// site can show when it was last built. Run automatically via the
// `prestart` / `prebuild-docs` npm hooks (cross-platform: Node, no shell tricks).
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';

const target = fileURLToPath(
  new URL('../projects/demo/src/app/build-info.ts', import.meta.url)
);

const timestamp = new Date().toISOString();
const contents = `// AUTO-GENERATED by scripts/generate-build-info.mjs — do not edit by hand.
export const BUILD_INFO = {
  timestamp: '${timestamp}',
};
`;

writeFileSync(target, contents);
console.log(`build-info.ts updated: ${timestamp}`);
